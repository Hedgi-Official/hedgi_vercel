const pathLanguageDetector = {
  name: 'path',
  lookup() {
    if (typeof window !== 'undefined') {
      const pathname = window.location.pathname;
      if (pathname === '/pt' || pathname.startsWith('/pt/')) {
        return 'pt-BR';
      }
    }
    return undefined;
  }
};

// Reads the `hedgi-geo` cookie set by the Express middleware that sees
// Vercel's x-vercel-ip-country header. Returns 'pt-BR' for Brazilian IPs
// so pt-BR becomes the default on /, /platforms, /developers, /what-is-hedge
// for BR visitors who haven't explicitly picked a language. Returns
// undefined for non-BR so the next detector in the order (navigator) wins.
const geoLanguageDetector = {
  name: 'geo',
  lookup() {
    if (typeof document === 'undefined') return undefined;
    const match = document.cookie.match(/(?:^|;\s*)hedgi-geo=([^;]+)/);
    if (!match) return undefined;
    const country = match[1].toUpperCase();
    if (country === 'BR') return 'pt-BR';
    return undefined;
  }
};

const languageDetector = new LanguageDetector();
languageDetector.addDetector(pathLanguageDetector);
languageDetector.addDetector(geoLanguageDetector);

i18n
  .use(languageDetector)
  .use(initReactI18next)
  .init({
    resources: {
      'en-US': enUS,
      'pt-BR': ptBR,
      'en': enUS,
      'pt': ptBR
    },
    supportedLngs: ['en-US', 'pt-BR', 'en', 'pt'],
    fallbackLng: 'en-US',
    detection: {
      order: ['path', 'localStorage', 'geo', 'navigator', 'htmlTag'],
      caches: typeof window !== 'undefined' ? ['localStorage'] : [],
      lookupLocalStorage: 'hedgi-language'
    },
    interpolation: {
      escapeValue: false
    }
  });

export default i18n;